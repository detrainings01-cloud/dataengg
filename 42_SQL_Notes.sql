--  DAILY REVENUE 
SELECT ORDER_DATE, SUM(TOTAL_SALES) AS TOTAL_SALES 
FROM FACT_SALES
GROUP BY ORDER_DATE 
ORDER BY ORDER_DATE ;

SELECT CURRENT_DATE;
SELECT DATE_TRUNC('month',CURRENT_DATE);

SELECT DATE_TRUNC('year',CURRENT_DATE);

SELECT DATE_TRUNC('MONTH',ORDER_DATE) AS MONTHLY_REVENUE, SUM(TOTAL_SALES) AS TOTAL_SALES 
FROM FACT_SALES 
GROUP BY DATE_TRUNC('MONTH',ORDER_DATE) 
ORDER BY DATE_TRUNC('MONTH',ORDER_DATE) ;

-- MONTHLY REVENUE 
SELECT DD.month_name AS MONTHLY_REVENUE, SUM(TOTAL_SALES) AS TOTAL_SALES 
FROM FACT_SALES SF  
JOIN DIM_DATE DD ON DD.date_id = SF.date_id
GROUP BY DD.month_name
ORDER BY TOTAL_SALES DESC ;

-- TOP SELLING PRODUCTS 
SELECT 
SF.PRODUCT_ID, SUM(SF.quantity_sold) AS QUANTITY_SOLD, SUM(SF.total_sales) AS TOTAL_SALES
FROM FACT_SALES SF
GROUP BY SF.PRODUCT_ID 
ORDER BY TOTAL_SALES DESC, QUANTITY_SOLD DESC
;

SELECT 
SF.PRODUCT_ID, PD.product_name, SUM(SF.quantity_sold) AS QUANTITY_SOLD, SUM(SF.total_sales) AS TOTAL_SALES
FROM FACT_SALES SF
JOIN DIM_PRODUCT PD ON SF.PRODUCT_ID = PD.PRODUCT_ID  
GROUP BY SF.PRODUCT_ID , PD.product_name
ORDER BY TOTAL_SALES DESC, QUANTITY_SOLD DESC
;

-- CUSTOMER LIFE TIME VALUE   (clv)

SELECT SF.CUSTOMER_ID, DC.CUSTOMER_NAME, SUM(SF.total_sales) AS TOTAL_SALES
FROM FACT_SALES SF
JOIN DIM_CUSTOMER DC ON DC.CUSTOMER_ID = SF.CUSTOMER_ID
GROUP BY SF.CUSTOMER_ID, DC.CUSTOMER_NAME
ORDER BY 3 DESC;


---  CUSTOMERS WITH REPEATED ORDERS 
SELECT 
CUSTOMER_ID, COUNT(ORDER_ID)
FROM FACT_SALES SF
GROUP BY CUSTOMER_ID 
HAVING COUNT(ORDER_ID) > 1 ;


-- CUSTOMER_METRICS 
SELECT 
SF.CUSTOMER_ID, CD.CUSTOMER_NAME, CD.CITY, CD.STATE, CD.COUNTRY, CD.signup_date,
MAX(ORDER_DATE) AS LAST_PURCHASED_DT, COUNT(ORDER_ID) AS COUNT_OF_ORDERS, SUM(TOTAL_SALES) AS TOTAL_REVENU
FROM FACT_SALES SF 
JOIN DIM_CUSTOMER CD  ON SF.CUSTOMER_ID = CD.CUSTOMER_ID 
GROUP BY SF.CUSTOMER_ID, CD.CUSTOMER_NAME, CD.CITY, CD.STATE, CD.COUNTRY, CD.signup_date;